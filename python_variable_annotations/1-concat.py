#!/usr/bin/env python3

"""
Concate two strings together is just smashing two words together.
"""


def concat(str1: str, str2: str) -> str:
    """_summary_

    Args:
        str1 (str): 1st one to be stuck together
        str2 (str): 2nd one,the tailend

    Returns:
        str: str1+str2  = str3.
    """
    return str1 + str2
