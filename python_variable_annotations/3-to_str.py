#!/usr/bin/env python3

"""
Converts a float to a string. uses function to_str.
"""


def to_str(n: float) -> str:
    """
    Converts a float to a string. uses function to_str.
    Args:
        n (float): float-point number to be converted

    Returns:
        str: string that represents the float.
    """
    return str(n)
